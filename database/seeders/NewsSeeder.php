    public function run(): void
    {
        $categories = NewsCategory::query()->get()->keyBy(fn (NewsCategory $c): string => (string) $c->getTranslation('label', 'tj'));
        $regions = Region::query()->get()->keyBy(fn (Region $r): string => (string) $r->getTranslation('name', 'tj'));

        foreach (array_values(self::NEWS) as $index => $item) {
            $slug = Transliterator::slug($item['title']);

            if (News::query()->where('slug', $slug)->exists()) {
                continue;
            }

            $bodyHtml = collect($item['body'])->map(fn (string $p): string => "<p>{$p}</p>")->implode('');

            // Keep the mock's descending date order stable across same-day items.
            $publishedAt = Carbon::createFromFormat('!d.m.Y', $item['date'])->setTime(12, 0)->subMinutes($index);

            News::create([
                'title' => ['tj' => $item['title'], 'ru' => $item['title']],
                'slug' => $slug,
                'category_id' => $categories->get($item['category'])?->id,
                'region_id' => $item['region'] !== null ? $regions->get($item['region'])?->id : null,
                'excerpt' => ['tj' => $item['excerpt'], 'ru' => $item['excerpt']],
                'body' => ['tj' => $bodyHtml, 'ru' => $bodyHtml],
                'author' => $item['author'],
                'status' => NewsStatus::Published,
                'published_at' => $publishedAt,
                'views' => $item['views'],
            ]);
        }
    }
